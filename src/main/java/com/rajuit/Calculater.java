package com.rajuit;

public class Calculater {

	public Integer add(Integer a,Integer b) {
		Integer sum=a+b;
		return sum;
	}
	
	public Integer mul(Integer a,Integer b) {
		Integer mul=a*b;
		return mul;
	}
	public Integer div(Integer a,Integer b) {
		Integer div=a/b;
		return div;
	}

	
}
